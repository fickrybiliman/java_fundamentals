package labs_examples.packages.labs.second;

public class SecondClass {
    public int eleven = 11;
    protected int twentytwo = 22;
    int  thirtythree = 33;
    private int fourtyfour = 44;

    public int division() {
        return fourtyfour / eleven;
    }

    protected int multiplication() {
        return eleven * twentytwo * thirtythree * fourtyfour;
    }
}
